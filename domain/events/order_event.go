// Package events содержит доменные события — факты, произошедшие в системе.
//
// ┌──────────────────────────────────────────────────────────────────────────┐
// │ ЧТО ТАКОЕ ДОМЕННОЕ СОБЫТИЕ?                                                │
// └──────────────────────────────────────────────────────────────────────────┘
//
// Доменное событие — это неизменяемый факт о том, что произошло в домене.
// В отличие от команды (сделай что-то), событие — это уже свершившийся факт.
//
// Соглашение по именованию: прошедшее время
//   ✅ OrderPlaced, OrderPaid, OrderCancelled
//   ❌ PlaceOrder, PayOrder (это команды, не события)
//
// ┌──────────────────────────────────────────────────────────────────────────┐
// │ ПОЧЕМУ СОБЫТИЯ В DOMAIN, А НЕ В INTERNAL?                                  │
// └──────────────────────────────────────────────────────────────────────────┘
//
// Domain — ядро приложения, не зависящее ни от чего.
// События — это контракт домена: "вот что может происходить в моей системе".
// Kafka, HTTP, gRPC — это детали инфраструктуры. Завтра заменишь Kafka на NATS —
// domain/events не изменится. Изменится только internal/events (реализация).
//
// Clean Architecture: зависимости всегда направлены ВНУТРЬ (к домену).
// domain → (ничего не зависит от)
// internal → зависит от domain
// pkg → зависит от domain и internal
package events

import "time"

// OrderEventType — тип события заказа.
//
// Константы в виде строк (не iota), потому что они сериализуются в JSON
// и хранятся в Kafka. Если использовать iota (0, 1, 2, ...) и добавить
// новое событие между существующими → все старые номера сдвинутся →
// консьюмеры будут неправильно интерпретировать исторические события.
// Строки безопасны: "order.paid" всегда означает "order.paid".
type OrderEventType string

const (
	// OrderPlaced — заказ оформлен (создан из корзины).
	// Публикуется после успешного PlaceOrder + commit транзакции.
	OrderPlaced OrderEventType = "order.placed"

	// OrderPaid — оплата подтверждена.
	// Публикуется после PayOrder. Можно использовать для:
	// - запуска сборки заказа на складе
	// - отправки email "Оплата получена"
	OrderPaid OrderEventType = "order.paid"

	// OrderShipped — заказ передан в доставку.
	// Публикуется после ShipOrder. Можно использовать для:
	// - отправки SMS с трек-номером
	// - обновления статуса в личном кабинете
	OrderShipped OrderEventType = "order.shipped"

	// OrderDelivered — заказ доставлен получателю.
	// Публикуется после DeliverOrder. Можно использовать для:
	// - начисления бонусных баллов
	// - запроса отзыва через 3 дня
	OrderDelivered OrderEventType = "order.delivered"

	// OrderCancelled — заказ отменён.
	// Публикуется после CancelOrder. Можно использовать для:
	// - инициации возврата денег
	// - восстановления stock товаров
	OrderCancelled OrderEventType = "order.cancelled"
)

// OrderEvent — событие жизненного цикла заказа.
//
// ┌──────────────────────────────────────────────────────────────────────────┐
// │ ЧТО ВКЛЮЧАТЬ В СОБЫТИЕ?                                                    │
// └──────────────────────────────────────────────────────────────────────────┘
//
// Правило: событие должно содержать достаточно данных, чтобы консьюмер мог
// действовать БЕЗ дополнительного обращения к БД.
//
// ❌ Плохо (thin event):
//   { "event_type": "order.paid", "order_id": "abc-123" }
//   Консьюмер вынужден: SELECT * FROM orders WHERE id = 'abc-123'
//   Это N+1 к БД от каждого консьюмера, coupling к схеме БД.
//
// ✅ Хорошо (fat event, наш вариант):
//   { "event_type": "order.paid", "order_id": "...", "user_id": "...",
//     "total_amount": 15000, "currency": "RUB", ... }
//   Консьюмер имеет всё необходимое для отправки уведомления.
//
// Компромисс: не включаем весь список OrderItems (большой payload).
// Если консьюмеру нужны items — пусть сам запросит по order_id.
//
// ┌──────────────────────────────────────────────────────────────────────────┐
// │ ПОЧЕМУ СУММА В КОПЕЙКАХ (int64), А НЕ float64?                              │
// └──────────────────────────────────────────────────────────────────────────┘
//
// float64 не подходит для денег из-за представления чисел с плавающей точкой:
//   0.1 + 0.2 = 0.30000000000000004 (в float64!)
// Для финансовых вычислений всегда используй целые числа (minor units):
//   1500 копеек = 15.00 рублей
// Это согласуется с тем, как Money хранится в нашей БД.
//
// ┌──────────────────────────────────────────────────────────────────────────┐
// │ ПОЧЕМУ EventID — UUID?                                                      │
// └──────────────────────────────────────────────────────────────────────────┘
//
// Идемпотентность у консьюмера. Kafka гарантирует at-least-once delivery:
// одно и то же сообщение может прийти дважды (при retry, rebalance).
// Консьюмер должен распознать дубликат:
//   "Я уже обработал event_id=abc-123? → пропускаю"
// Без EventID консьюмер не может отличить дубликат от нового события.
type OrderEvent struct {
	// EventID — уникальный идентификатор события (не заказа!).
	// Используется консьюмерами для идемпотентной обработки.
	// Каждый раз генерируется новый UUID, даже для одного заказа.
	EventID string `json:"event_id"`

	// EventType — тип произошедшего события (одна из констант выше).
	EventType OrderEventType `json:"event_type"`

	// OccurredAt — момент возникновения события (UTC).
	// НЕ время записи в Kafka (это отличается из-за сетевых задержек).
	// Важно для аудит-логов и аналитики.
	OccurredAt time.Time `json:"occurred_at"`

	// OrderID — идентификатор заказа.
	// Используется как ключ партиции Kafka: все события одного заказа
	// попадают в одну партицию → гарантированный порядок для заказа.
	OrderID string `json:"order_id"`

	// UserID — кому принадлежит заказ.
	// Нужен консьюмерам для уведомлений, не заставляя их делать JOIN.
	UserID string `json:"user_id"`

	// Status — текущий статус заказа после события.
	// Консьюмер знает, в каком состоянии заказ прямо сейчас.
	Status string `json:"status"`

	// TotalAmount — сумма заказа в минимальных единицах валюты (копейках).
	// Не float64! Причина объяснена выше.
	TotalAmount int64 `json:"total_amount"`

	// Currency — код валюты по ISO 4217 (RUB, USD, EUR).
	Currency string `json:"currency"`
}
