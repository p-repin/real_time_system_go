// Package kafka содержит низкоуровневые клиенты для работы с Kafka.
//
// ┌──────────────────────────────────────────────────────────────────────────┐
// │ ЗАЧЕМ ОБЁРТКА, ЕСЛИ KAFKA-GO И ТАК ПРОСТОЙ?                               │
// └──────────────────────────────────────────────────────────────────────────┘
//
// pkg/kafka — инфраструктурный слой. Он знает о Kafka-специфике:
// конфигурация Writer'а, сжатие, таймауты, graceful close.
//
// internal/events — слой бизнес-событий. Он знает о доменных событиях:
// OrderEvent, его сериализация, логика публикации.
//
// Разделение позволяет:
// 1. В тестах заменить Producer на mock (через интерфейс)
// 2. Сменить библиотеку с kafka-go на confluent-kafka-go без изменения бизнес-кода
// 3. Добавить метрики/трейсинг в одном месте
//
// ┌──────────────────────────────────────────────────────────────────────────┐
// │ ПОЧЕМУ pkg/, А НЕ internal/?                                               │
// └──────────────────────────────────────────────────────────────────────────┘
//
// pkg/ — переиспользуемый код, который мог бы жить в отдельном модуле.
// Этот Producer не знает ничего о нашем домене (Order, User).
// Его можно скопировать в другой проект и он будет работать.
//
// internal/ — код специфичный для ЭТОГО проекта.
// internal/events знает об OrderEvent — это наш домен.
package kafka

import (
	"context"
	"fmt"
	"time"

	"github.com/segmentio/kafka-go"
	"real_time_system/internal/config"
)

// Message — сообщение для публикации в Kafka.
//
// Мы не используем kafka.Message напрямую в бизнес-коде,
// потому что это создало бы зависимость от kafka-go во всём проекте.
// Если завтра сменим библиотеку — переписываем только этот файл.
type Message struct {
	// Key — ключ партиционирования.
	// Kafka гарантирует, что сообщения с одинаковым ключом попадают
	// в одну и ту же партицию → гарантированный порядок для этого ключа.
	//
	// Для событий заказа: Key = OrderID.
	// Все события одного заказа (placed→paid→shipped→delivered) попадают
	// в одну партицию и обрабатываются консьюмером по порядку.
	//
	// ❌ Ошибка: пустой ключ → Kafka распределяет сообщения по round-robin →
	//   события одного заказа могут прийти в произвольном порядке.
	Key []byte

	// Value — полезная нагрузка (payload). Обычно JSON.
	Value []byte
}

// Producer — обёртка над kafka.Writer для отправки сообщений.
//
// ┌──────────────────────────────────────────────────────────────────────────┐
// │ kafka.Writer vs kafka.Conn: почему Writer?                                 │
// └──────────────────────────────────────────────────────────────────────────┘
//
// kafka.Conn — низкоуровневый TCP-коннект к конкретному брокеру.
//   + Максимальный контроль
//   - Сам управляй балансировкой между брокерами
//   - Сам обрабатывай failover при падении брокера
//   - Много boilerplate кода
//
// kafka.Writer — высокоуровневый клиент с автобалансировкой:
//   + Сам выбирает партицию (по ключу через hash или round-robin)
//   + Сам переподключается при падении брокера
//   + Batching сообщений для эффективности
//   + Всё, что нужно для production
//
// Для 99% use-cases нужен Writer.
type Producer struct {
	writer *kafka.Writer
}

// NewProducer создаёт Producer с настроенным Writer'ом.
//
// ┌──────────────────────────────────────────────────────────────────────────┐
// │ ПАРАМЕТРЫ WRITER'А: ПОЧЕМУ ТАК?                                            │
// └──────────────────────────────────────────────────────────────────────────┘
func NewProducer(cfg config.KafkaConfig) *Producer {
	writer := &kafka.Writer{
		// Addr — адрес bootstrap-сервера.
		// Writer подключается к нему, получает метаданные о кластере
		// (список брокеров, партиции), и затем пишет напрямую в нужный брокер.
		// Один адрес достаточен для discovery, даже если брокеров 10.
		//
		// ❌ Частая ошибка: при запуске go run (вне Docker) указать "kafka:9092"
		//   → "dial tcp: lookup kafka: no such host"
		//   Решение: localhost:9094 для локального запуска, kafka:9092 для Docker.
		Addr: kafka.TCP(cfg.Brokers...),

		// Balancer — алгоритм выбора партиции.
		// Hash балансировщик: hash(key) % num_partitions = номер партиции.
		// Одинаковый ключ → всегда одна и та же партиция → порядок гарантирован.
		//
		// Альтернативы:
		//   kafka.RoundRobin{} — равномерное распределение, но без гарантии порядка
		//   kafka.LeastBytes{} — в партицию с меньшим объёмом данных
		//
		// Для event-driven систем с ключом: всегда Hash.
		Balancer: &kafka.Hash{},

		// WriteTimeout — сколько ждать подтверждения от брокера.
		// После этого времени WriteMessages вернёт ошибку.
		// БЕЗ ТАЙМАУТА: при недоступном брокере горутина заблокируется навсегда.
		WriteTimeout: cfg.WriteTimeout,

		// ReadTimeout — таймаут на чтение ответа (подтверждения) от брокера.
		ReadTimeout: 10 * time.Second,

		// RequiredAcks — сколько брокеров должны подтвердить запись.
		// kafka.RequireOne (1) — только leader-брокер.
		// kafka.RequireAll (-1) — все in-sync replicas.
		//
		// ❌ Ошибка: RequireAll при replication.factor=1 (один брокер, как у нас).
		//   Kafka не видит разницы, но если поставить RequireAll при недостаточном
		//   количестве in-sync replicas → "Not enough in-sync replicas"
		RequiredAcks: kafka.RequiredAcks(cfg.RequiredAcks),

		// AllowAutoTopicCreation — создать топик автоматически если не существует.
		// Удобно для локальной разработки: не нужно создавать топик вручную.
		// В production лучше false: неправильное имя топика → сразу ошибка,
		// а не тихое создание нового топика со значениями по умолчанию.
		AllowAutoTopicCreation: true,

		// Async: false (по умолчанию) — синхронная запись.
		// Writer ждёт подтверждения от Kafka перед возвратом из WriteMessages.
		//
		// Async: true — асинхронная запись.
		//   + Максимальная скорость
		//   - Ошибки нельзя обработать в вызывающем коде (нужен ErrorLogger)
		//   - При сбое в момент flush — сообщения теряются без уведомления
		//
		// Для нашего проекта: false (синхронно). Мы хотим знать об ошибках.
	}

	return &Producer{writer: writer}
}

// Publish отправляет одно сообщение в указанный топик.
//
// ┌──────────────────────────────────────────────────────────────────────────┐
// │ ПОЧЕМУ topic ПЕРЕДАЁТСЯ КАК ПАРАМЕТР, А НЕ В WRITER?                       │
// └──────────────────────────────────────────────────────────────────────────┘
//
// kafka.Writer можно создать с фиксированным топиком (field Topic: "orders").
// Тогда метод Publish был бы проще. Но мы создаём ONE Producer на всё приложение,
// который может писать в РАЗНЫЕ топики (orders, notifications, analytics).
//
// Если бы Topic был в Writer → нужен отдельный Writer (и соединение!) для каждого топика.
// С передачей топика в сообщении — один Writer, много топиков. Эффективнее.
func (p *Producer) Publish(ctx context.Context, topic string, msg Message) error {
	err := p.writer.WriteMessages(ctx, kafka.Message{
		Topic: topic,
		Key:   msg.Key,
		Value: msg.Value,
	})
	if err != nil {
		// Оборачиваем ошибку с контекстом: в логах будет видно какой топик
		return fmt.Errorf("publish to topic %q: %w", topic, err)
	}
	return nil
}

// Close корректно завершает работу Producer'а.
//
// ┌──────────────────────────────────────────────────────────────────────────┐
// │ ЧТО ПРОИСХОДИТ ПРИ Close?                                                  │
// └──────────────────────────────────────────────────────────────────────────┘
//
// kafka.Writer буферизирует сообщения для батчинга (эффективности).
// Close() flush'ит (сбрасывает) все буферизированные сообщения и закрывает соединения.
//
// ❌ Если не вызвать Close при shutdown:
//   - Буферизированные сообщения ТЕРЯЮТСЯ
//   - TCP-соединения с брокером не закрываются корректно
//   - Брокер думает, что producer упал → timeout на его стороне
//
// ✅ Всегда вызывай Close в Server.shutdown():
//   s.kafkaProducer.Close()
func (p *Producer) Close() error {
	return p.writer.Close()
}
